/** @format */

import { api } from '@/lib/api';

export const TenantService = {
	//////////////////////////////////////////////////////
	// CREATE TENANT (AUTH REQUIRED)
	//////////////////////////////////////////////////////
	create: (data: any) => api.post('/tenants', data),

	//////////////////////////////////////////////////////
	// GET TENANTS FOR CURRENT USER (WORKSPACE SWITCHER)
	//////////////////////////////////////////////////////
	getMyTenants: () => api.get('/tenants/me'),

	//////////////////////////////////////////////////////
	// GET TENANT CONTEXT (RBAC + PERMISSIONS)
	//////////////////////////////////////////////////////
	getContext: (tenantId: string) => api.get(`/tenants/${tenantId}/context`),

	//////////////////////////////////////////////////////
	// GET SINGLE TENANT
	//////////////////////////////////////////////////////
	getOne: (id: string) => api.get(`/tenants/${id}`),

	//////////////////////////////////////////////////////
	// UPDATE TENANT
	//////////////////////////////////////////////////////
	update: (id: string, data: any) => api.put(`/tenants/${id}`, data),

	//////////////////////////////////////////////////////
	// DELETE TENANT
	//////////////////////////////////////////////////////
	remove: (id: string) => api.delete(`/tenants/${id}`),

	//////////////////////////////////////////////////////
	// ADMIN ONLY (OPTIONAL)
	//////////////////////////////////////////////////////
	getAll: () => api.get('/tenants'),
};
